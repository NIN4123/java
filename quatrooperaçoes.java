public class quatrooperaçoes {
    public static void main(String[] args){
        int a = 7;
        int b = 5;

        int soma = a + b;
        int mult = a*b;
        int sub = a-b;
        int quoci = a/b;

        System.out.println("soma: " + soma);
        System.out.println("multiplicação: " + mult);
        System.out.println("subtração: " + sub);
        System.out.println("divisao: " + quoci);
    }
}
